import React, { Component } from "react";

import Dropzone from "react-dropzone";

import { DropContainer, UploadMessage } from "./styles";

export default class Upload extends Component {
  renderDragMessage = (isDragActive, isDragReject) => {
    if (!isDragActive) {
    return <UploadMessage>{this.props.label || "Anexar arquivos do projeto"}</UploadMessage>;
    }

    if (isDragReject) {
      return <UploadMessage type="error">Arquivo n√£o suportado</UploadMessage>;
    }

    return <UploadMessage type="success">Solte os arquivos aqui</UploadMessage>;
  };

  render() {
    const { onUpload, filesAccepted } = this.props;

    return (
      <Dropzone accept={filesAccepted || ".mp4, .mov, .pdf, .doc, .docx, .mp3, .mpeg"} onDropAccepted={onUpload}>
        {({ getRootProps, getInputProps, isDragActive, isDragReject }) => (
          <DropContainer
            {...getRootProps()}
            isDragActive={isDragActive}
            isDragReject={isDragReject}
          >
            <input {...getInputProps()} />
            {this.renderDragMessage(isDragActive, isDragReject)}
          </DropContainer>
        )}
      </Dropzone>
    );
  }
}

